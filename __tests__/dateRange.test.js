import { dateRange } from '../es';

test('dateRange', () => {
  expect(
    dateRange(new Date('2020-05-20 05:20'), new Date('2020-05-20 05:30'), {
      rangeType: 'minute',
    })
  ).toEqual([
    '2020-05-20 05:20:00',
    '2020-05-20 05:21:00',
    '2020-05-20 05:22:00',
    '2020-05-20 05:23:00',
    '2020-05-20 05:24:00',
    '2020-05-20 05:25:00',
    '2020-05-20 05:26:00',
    '2020-05-20 05:27:00',
    '2020-05-20 05:28:00',
    '2020-05-20 05:29:00',
    '2020-05-20 05:30:00',
  ]);
});
